-module(webmachine_example_app_hello).
-export([init/1, to_html/2]).

-compile([{parse_transform, lager_transform}]).

%% ###############################################################
%% INCLUDE
%% ###############################################################

-include_lib("webmachine/include/webmachine.hrl").

%% ###############################################################
%% CONTROL
%% ###############################################################

init([]) ->
    {ok, []}.

%% ###############################################################
%% RESOURCE
%% ###############################################################

to_html(ReqData, State) ->
    lager:info("Serving request for ~s ...", [wrq:base_uri(ReqData)]),
    {"<html><body>Hello world from cloudControl</body></html>", ReqData, State}.

